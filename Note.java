
public class Note{

  // Variables/Attribute
  private String color, message;
  private int x, y, width, height, pins;

  // Default constructor for note
  public Note(int x, int y, int width, int height, String color, String message){

    // Initialize all variables
    this.x = x;
    this.y = y;
    this.width = width;
    this.height = height;
    this.color = color;
    this.message = message;
    this.pins = 0;
  }




}
